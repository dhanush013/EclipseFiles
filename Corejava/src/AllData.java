
public class AllData {
	
	public static void main(String[] args) {
		int x=65656565;// decimal - regular input.
		int y=0b101010;// binary - it starts with 0b
		int z=0x312abc;// hexadecimal - it starts with 0x
		int a=0757;//octal - it starts with zero (0)
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(a);
		
	}

}
